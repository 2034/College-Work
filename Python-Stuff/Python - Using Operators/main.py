first_number =  int(input("Enter first number: "))
second_number =  int(input("Enter second number: "))

sumnum = first_number + second_number
subnum = first_number - second_number

print(f"Adding {first_number} and {second_number} is {sumnum}")
print(f"Subtracting {first_number} and {second_number} is {subnum}")